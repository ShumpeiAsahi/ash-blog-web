import Head from "next/head";
import { Card } from "../components/Card";
import { Footer } from "../components/Footer";
import { Header } from "../components/Header";
import fs from "fs";
import matter from "gray-matter";
import { Pagination } from "../components/Pagination";
import { PAGE_SIZE, range } from "../libs/tools";

type PageProps = {
  posts: {
    slug: string;
    data: {
      [key: string]: any;
    };
  }[];
  pages: number[];
  current_page: number;
};

export const getStaticProps = () => {
  const current_page = 1;
  const files = fs.readdirSync("posts");
  const posts = ([] = files.map((fileName) => {
    const slug = fileName.replace(/\.md$/, "");
    const fileContent = fs.readFileSync(`posts/${fileName}`, "utf-8");
    const { data } = matter(fileContent);
    return { slug, data };
  }));

  const pages = range(1, Math.ceil(posts.length / PAGE_SIZE));

  const sortedPosts = posts.sort((postA, postB) =>
    new Date(postA.data.date) > new Date(postB.data.date) ? -1 : 1
  );

  const slicedPosts = sortedPosts.slice(
    PAGE_SIZE * (current_page - 1),
    PAGE_SIZE * current_page
  );

  return { props: { posts: slicedPosts, pages, current_page } };
};

const Home: React.FC<PageProps> = ({ posts, pages, current_page }) => {
  return (
    <div>
      <Head>
        <title>人生は神ゲー</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />

      <main>
        <div className="w-full md:w-8/12 mx-auto">
          <div>
            <h1 className="text-lg font-bold my-16 text-center">
              東京在住のエンジニアブログ
            </h1>
          </div>
          {posts.map((post) => {
            return (
              <Card
                key={post.slug}
                slug={post.slug}
                title={post.data.title}
                date={post.data.date}
                description={post.data.description}
                thumbnail={post.data.thumbnail}
              />
            );
          })}
        </div>
        <Pagination pages={pages} current_page={current_page} />
      </main>
      <Footer />
    </div>
  );
};

export default Home;
